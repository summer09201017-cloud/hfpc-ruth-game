// 隨機抽 n 題(不重複),回傳題目索引陣列——給「標題練習」用。
export function pickQuestions(n) {
  const idx = QUESTIONS.map((_, i) => i)
  for (let i = idx.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    const tmp = idx[i]
    idx[i] = idx[j]
    idx[j] = tmp
  }
  return idx.slice(0, Math.min(n, idx.length))
}

// 結算鼓勵語(依答對比例)。
export function quizRemark(correct, total) {
  const r = total ? correct / total : 0
  if (r >= 1) return '全對!你把約拿的故事記得很熟,願這故事也記在心裡。'
  if (r >= 0.6) return '答得很好!再讀一遍約拿書,會更清楚神的心意。'
  return '沒關係,故事多讀幾次就記住了。重要的是認識這位尋回約拿的神。'
}
